def get_subimage(image, pos, size, default = '.'):
    d = floor(size / 2)
    binstr = ''
    for y in range(pos[1] - d, pos[1] + d + 1):
        for x in range(pos[0] - d, pos[0] + d + 1):
            if y < 0 or y >= len(image) or x < 0 or x >= len(image[0]): val = default
            else: val = image[y][x]
            binstr += '1' if val == '#' else '0'
    
    return int(binstr, base=2)

